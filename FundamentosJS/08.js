// Array
const tecnologias = [20, 30, true, 8 * 5]; // This is called an array; what's on the left and right is the array itself.
// Arrays always start from index 0 up to however many elements you want.

console.log(tecnologias[3]); // Accessing the element at index 3 (fourth element), which is 40 (result of 8 * 5).

console.table(tecnologias); // Prints the entire array in a table format for better visibility.

console.log(tecnologias.length); // Returns the number of elements in the array (4 in this case).

console.log(tecnologias.toString()); // Converts the array to a string, resulting in "20,30,true,40".
